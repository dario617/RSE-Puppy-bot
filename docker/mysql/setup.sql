USE grafana;

CREATE TABLE measurements ( 
	temp1_cur DOUBLE(16,2) DEFAULT 0.00,
	temp1_max DOUBLE(16,2) DEFAULT 0.00,
	temp2_cur DOUBLE(16,2) DEFAULT 0.00,
	temp2_max DOUBLE(16,2) DEFAULT 0.00,
	temp3_cur DOUBLE(16,2) DEFAULT 0.00,
	temp3_max DOUBLE(16,2) DEFAULT 0.00,
	temp4_cur DOUBLE(16,2) DEFAULT 0.00,
	temp4_max DOUBLE(16,2) DEFAULT 0.00,
	mem_dsp DOUBLE(16,2) DEFAULT 0.00,
	mem_use DOUBLE(16,2) DEFAULT 0.00,
	dsk_dsp DOUBLE(16,2) DEFAULT 0.00,
	disk_use DOUBLE(16,2) DEFAULT 0.00,
	CPU DOUBLE(16,2) DEFAULT 0.00,
    measure_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    insertion_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY(measure_time)
);
